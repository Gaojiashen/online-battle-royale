def get_player_time_info(player_id):
    """获取玩家的时间和时段信息"""
    conn = get_db()
    row = conn.execute(
        "SELECT id, name, game_day, game_time FROM players WHERE id = ?",
        (player_id,)
    ).fetchone()
    conn.close()
    if not row:
        return None

    minutes = row["game_time"]  # 0-1440
    hours = minutes // 60
    mins = minutes % 60
    time_str = f"{hours:02d}:{mins:02d}"

    # 时段判定
    if 480 <= minutes < 1200:  # 8:00-20:00
        period = "day"
        period_label = "☀️ 白天"
    elif 1200 <= minutes < 1320:  # 20:00-22:00
        period = "night"
        period_label = "🌙 夜晚"
    else:  # 22:00-次日8:00
        period = "deep_night"
        period_label = "💤 深夜"

    return {
        "player_id": row["id"],
        "player_name": row["name"],
        "game_day": row["game_day"],
        "game_time": minutes,
        "time_str": time_str,
        "period": period,
        "period_label": period_label,
    }


def advance_game_time(player_id, cost_minutes):
    """推进游戏时间，返回新的时间信息。自动处理跨日。"""
    conn = get_db()
    row = conn.execute(
        "SELECT game_day, game_time FROM players WHERE id = ?", (player_id,)
    ).fetchone()
    if not row:
        conn.close()
        return None

    new_time = row["game_time"] + cost_minutes
    new_day = row["game_day"]

    # 处理跨日: 如果超过22:00(1320分钟)，推进到次日8:00
    while new_time >= 1320:  # 超过22:00
        new_day += 1
        new_time = 480 + (new_time - 1320)  # 次日8:00 + 超出部分

    # 如果时间在深夜(22:00-次日8:00)，也推进
    # (理论上上面的while循环已处理)

    conn.execute(
        "UPDATE players SET game_day = ?, game_time = ? WHERE id = ?",
        (new_day, new_time, player_id)
    )
    conn.commit()
    conn.close()

    hours = new_time // 60
    mins = new_time % 60
    period = "day" if 480 <= new_time < 1200 else ("night" if 1200 <= new_time < 1320 else "deep_night")

    return {
        "game_day": new_day,
        "game_time": new_time,
        "time_str": f"{hours:02d}:{mins:02d}",
        "period": period,
        "crossed_day": new_day > row["game_day"],
    }


