def _seed_locations(cur, conn):
    """写入28个区域及连接数据（如不存在则创建）"""
    existed = cur.execute("SELECT COUNT(*) FROM locations").fetchone()[0]
    if existed >= 28:
        return  # 已有完整数据

    # 建立region_num -> location_id的映射
    region_to_id = {}
    for region_num, name, english, desc, ttype in LOCATION_SEED:
        cur.execute(
            "INSERT INTO locations (region_num, name, english, description, travel_type) "
            "VALUES (?, ?, ?, ?, ?)",
            (region_num, name, english, desc, ttype))
        region_to_id[region_num] = cur.lastrowid

    # 写入连接
    for row in LOCATION_CONNECTIONS_SEED:
        src_reg = row[0]
        tgt_reg = row[1]
        time = row[2]
        special = row[3] if len(row) > 3 else False
        src_id = region_to_id.get(src_reg)
        tgt_id = region_to_id.get(tgt_reg)
        if src_id and tgt_id:
            cur.execute(
                "INSERT INTO location_connections (location_id, target_id, travel_time, is_special) "
                "VALUES (?, ?, ?, ?)",
                (src_id, tgt_id, time, 1 if special else 0))

    conn.commit()


