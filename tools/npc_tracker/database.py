def _seed_options_batch(conn, npc_name, nodes_list, options_list):
    """通用种子：为指定NPC写入节点和选项"""
    npc_row = conn.execute("SELECT id FROM npcs WHERE name = ?", (npc_name,)).fetchone()
    if not npc_row:
        return
    npc_id = npc_row[0]

    # 检查是否已有节点
    existed = conn.execute(
        "SELECT COUNT(*) FROM storyline_nodes WHERE npc_id = ?", (npc_id,)
    ).fetchone()[0]
    if existed > 0:
        return

    conn.execute("UPDATE npcs SET has_storyline = 1 WHERE id = ?", (npc_id,))

    # 写入节点
    nodes_map = {}
    for num, title, loc, desc, hint in nodes_list:
        cur = conn.execute(
            "INSERT INTO storyline_nodes (npc_id, node_number, title, location, description, hint) "
            "VALUES (?, ?, ?, ?, ?, ?)", (npc_id, num, title, loc, desc, hint))
        nodes_map[num] = cur.lastrowid

    # 写入选项
    for node_num, key, opt_text, result_text, gain in options_list:
        if node_num in nodes_map:
            conn.execute(
                "INSERT INTO node_options (node_id, option_key, option_text, result_text, player_gain) "
                "VALUES (?, ?, ?, ?, ?)",
                (nodes_map[node_num], key, opt_text, result_text, gain))


def _seed_connections(conn):
    """写入NPC关联数据"""
    existed = conn.execute("SELECT COUNT(*) FROM npc_connections").fetchone()[0]
    if existed > 0:
        return

    # 建立name->id映射
    name_to_id = {}
    for row in conn.execute("SELECT id, name FROM npcs").fetchall():
        name_to_id[row["name"]] = row["id"]

    for n1, n2, rel, desc, intersection in CONNECTIONS_SEED:
        id1 = name_to_id.get(n1)
        id2 = name_to_id.get(n2)
        if id1 and id2:
            conn.execute(
                "INSERT INTO npc_connections (npc_id_1, npc_id_2, relationship, description, intersection_node) "
                "VALUES (?, ?, ?, ?, ?)", (id1, id2, rel, desc, intersection))


